# professional communication
# ------------

# be on time
    # don't be late. communicate if you will be late
# actively listen
    # listen to their full account. 
    # avoid interrupting them. (if they go on tangents, redirect focus)
    # don't do other things.
# maintain positive/confident/friendly attitude
    # don't be judgmental
# know your audience
    # avoid jargon
# clearly communicate timeline/status/cost expectations
# protect confidentiality of sensitive info/data

