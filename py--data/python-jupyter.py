# ---------------
# jupyter shortcuts
# ---------------

# esc -- command mode
    # a -- insert cell above
    # b -- insert cell below
    # up -- select cell above
    # down -- select cell below
    # shift+up -- extend up
    # shift+down -- extend down

    # c -- copy selected cells
    # x -- cut selected cells
    # v -- paste cells below
    # dd -- delete cells
    # z -- undo cell deletion

    # shift+enter -- run current cell
    # ctrl+enter -- run selected cells
    # ctrl+s -- save
    # h -- show keyboard shortcuts
    # ii -- interrupt kernel

# enter -- edit mode
    # tab -- show autocomplete
    # shift+tab -- show docstring for method
    # `help(object)` -- show data about object (methods, etc)
