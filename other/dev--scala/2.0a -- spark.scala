// hadoop allows distribution of very large files across multiple machines.
// hadoop distributed file system (hdfs)
// HDFS allows for parity and redundancy (I think these are the correct terms lol)
// MapReduce allows for computation on that data.

// spark scalable because files/processes can be distributed.
// spark improves upon the concepts in hdfs/mapreduce.
// spark can use various forms of data (cassandra, aws s3, hdfs, etc)
// Resilient Distributed Dataset (RDD) has 4 main features
    // distributed colleciton of data
    // fault tolerance
    // parallel operation
    // able to consume data from various data sources
// RDD operations (kinda similar to TF graphs and sessions)
    // transformations -- instructions to follow
    // actions -- carrying out instructions and returning something.
// RDDs are immutable, lazily evaluated, and cacheable
// Spark 2.0 includes dataframes

